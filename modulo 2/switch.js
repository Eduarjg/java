/*
switch cases --> estrutura de controle
*/
const temperature = 30
switch (temperature) {
    case 10:
        console.log("temperatura normal")


        break;
    case 15:
        console.log("temperatura medio frio")



        break;
    case 20:

    console.log("temperatura quente")



        break;
    case 30:
        console.log("temperatura muito quente")



        break;


    default:

    console.log("temperatura desconhecida")


        break;
}